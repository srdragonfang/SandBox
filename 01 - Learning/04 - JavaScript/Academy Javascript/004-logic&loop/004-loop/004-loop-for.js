// **Nesting For Loops**
// If you have a multi-dimensional array,
// you can use the same logic as the prior waypoint to loop through both the array and any sub-arrays.
// Here is an example:

const arr = [
	[1, 2],
	[3, 4],
	[5, 6],
];

for (let i = 0; i < arr.length; i++) {
	for (let j = 0; j < arr[i].length; j++) {
		cosole.log(arr[i][j]);
	}
}
